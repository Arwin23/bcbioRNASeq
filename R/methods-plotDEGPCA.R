#' Plot DEG PCA
#'
#' @name plotDEGPCA
#' @author Michael Steinbaugh
#'
#' @inheritParams general
#' @inheritParams plotPCA
#' @inheritParams plotDEGHeatmap
#'
#' @examples
#' # DESeqResults, DESeqTransform ====
#' plotDEGPCA(
#'     object = res_small,
#'     counts = rld_small,
#'     interestingGroups = interestingGroups(bcb_small)
#' )
NULL



# Methods ======================================================================
#' @rdname plotDEGPCA
#' @export
setMethod(
    "plotDEGPCA",
    signature(
        object = "DESeqResults",
        counts = "DESeqTransform"
    ),
    function(
        object,
        counts,
        interestingGroups = "sampleName",
        lfc = 0L,
        color = scale_color_viridis(discrete = TRUE),
        label = FALSE,
        returnData = FALSE
    ) {
        # Passthrough: interestingGroups, color, label, returnData
        assert_is_a_number(lfc)
        assert_all_are_non_negative(lfc)

        # Get the DE gene vector using `resultsTables()`
        list <- resultsTables(
            object,
            lfc = lfc,
            rowData = NULL,
            summary = FALSE,
            write = FALSE
        )
        genes <- c(
            list[["degLFCUp"]][["geneID"]],
            list[["degLFCDown"]][["geneID"]]
        )

        .plotPCA.DESeqTransform(
            object = counts,
            interestingGroups = interestingGroups,
            genes = genes,
            color = color,
            label = label,
            returnData = returnData
        )
    })
