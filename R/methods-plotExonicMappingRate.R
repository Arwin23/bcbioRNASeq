#' Plot Exonic Mapping Rate
#'
#' @rdname plotExonicMappingRate
#' @name plotExonicMappingRate
#' @family Quality Control Plots
#' @author Michael Steinbaugh, Rory Kirchner, Victor Barrera
#'
#' @inherit plotTotalReads
#'
#' @examples
#' load(system.file(
#'     file.path("extdata", "bcb.rda"),
#'     package = "bcbioRNASeq"))
#'
#' # bcbioRNASeq
#' plotExonicMappingRate(bcb)
#' plotExonicMappingRate(
#'     bcb,
#'     interestingGroups = "sampleName",
#'     fill = NULL)
#'
#' # data.frame
#' df <- metrics(bcb)
#' plotExonicMappingRate(df)
NULL



# Constructors =================================================================
#' @importFrom basejump uniteInterestingGroups
#' @importFrom ggplot2 aes_ coord_flip geom_bar ggplot guides labs ylim
#' @importFrom viridis scale_fill_viridis
.plotExonicMappingRate <- function(
    object,
    interestingGroups = "sampleName",
    passLimit = 60,
    fill = viridis::scale_fill_viridis(discrete = TRUE),
    flip = TRUE,
    title = TRUE) {
    if (isTRUE(title)) {
        title <- "exonic mapping rate"
    } else if (!is.character(title)) {
        title <- NULL
    }

    metrics <- uniteInterestingGroups(object, interestingGroups)
    p <- ggplot(
        metrics,
        mapping = aes_(
            x = ~sampleName,
            y = ~exonicRate * 100,
            fill = ~interestingGroups)
    ) +
        geom_bar(stat = "identity") +
        labs(title = title,
             x = "sample",
             y = "exonic mapping rate (%)",
             fill = paste(interestingGroups, collapse = ":\n")) +
        ylim(0, 100)

    if (is.numeric(passLimit)) {
        p <- p + qcPassLine(passLimit)
    }

    if (is(fill, "ScaleDiscrete")) {
        p <- p + scale_fill_viridis(discrete = TRUE)
    }

    if (isTRUE(flip)) {
        p <- p + coord_flip()
    }

    if (identical(interestingGroups, "sampleName")) {
        p <- p + guides(fill = FALSE)
    }

    p
}



# Methods ======================================================================
#' @rdname plotExonicMappingRate
#' @export
setMethod(
    "plotExonicMappingRate",
    signature("bcbioRNASeq"),
    function(
        object,
        interestingGroups,
        passLimit = 60,
        fill = viridis::scale_fill_viridis(discrete = TRUE),
        flip = TRUE,
        title = TRUE) {
        if (is.null(metrics(object))) return(NULL)
        if (missing(interestingGroups)) {
            interestingGroups <- basejump::interestingGroups(object)
        }
        .plotExonicMappingRate(
            metrics(object),
            interestingGroups = interestingGroups,
            passLimit = passLimit,
            fill = fill,
            flip = flip,
            title = title)
    })



#' @rdname plotExonicMappingRate
#' @export
setMethod(
    "plotExonicMappingRate",
    signature("data.frame"),
    .plotExonicMappingRate)
