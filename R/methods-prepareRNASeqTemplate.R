#' Prepare RNA-Seq R Markdown Template
#'
#' @rdname prepareRNASeqTemplate
#' @name prepareRNASeqTemplate
#' @author Michael Steinbaugh
#'
#' @inheritParams AllGenerics
#'
#' @return No value.
#'
#' @examples
#' prepareRNASeqTemplate()
#'
#' # Clean up
#' unlink(c(
#'     "_footer.Rmd",
#'     "_header.Rmd",
#'     "_output.yaml",
#'     "_setup.R",
#'     "bibliography.bib",
#' ))
NULL



# Constructors =================================================================
.prepareRNASeqTemplate <- function(object) {
    prepareTemplate(
        c(
            "_footer.Rmd",
            "_header.Rmd",
            "_output.yaml",
            "_setup.R",
            "bibliography.bib"
        ),
        sourceDir = system.file(
            "rmarkdown/shared",
            package = "bcbioRNASeq")
    )
}


# Methods ======================================================================
#' @rdname prepareRNASeqTemplate
#' @importFrom bcbioBase prepareTemplate
#' @export
setMethod(
    "prepareRNASeqTemplate",
    signature("missing"),
    .prepareRNASeqTemplate)
