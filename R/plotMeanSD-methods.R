#' Plot Row Standard Deviations vs. Row Means
#'
#' [vsn::meanSdPlot()] wrapper that plots count transformations on a log2 scale.
#'
#' - DESeq2 log2: log2 size factor-adjusted normalized counts.
#' - DESeq2 rlog: **r**egularized **log** transformation.
#' - DESeq2 VST: **v**ariance-**s**tabilizing **t**ransformation.
#' - edgeR log2 TMM: log2 **t**rimmed **m**ean of **M**-values transformation.
#'
#' @seealso
#' - [vsn::meanSdPlot()].
#' - [DESeq2::DESeq()].
#' - [DESeq2::rlog()].
#' - [DESeq2::varianceStabilizingTransformation()].
#' - [edgeR::calcNormFactors()].
#'
#' @name plotMeanSD
#' @author Michael Steinbaugh, Lorena Patano
#'
#' @inheritParams general
#' @inheritParams bcbioRNASeq
#'
#' @return `ggplot` grid.
#'
#' @examples
#' data(bcb, deseq)
#'
#' ## bcbioRNASeq ====
#' plotMeanSD(bcb)
#'
#' ## DESeqAnalysis ====
#' plotMeanSD(deseq)
NULL



# Internal plot grid constructor ===============================================
# Match the vst, rlog conventions to `bcbioRNASeq()` generator.
.plotMeanSD <- function(
    raw,
    normalized,
    vst = NULL,
    rlog = NULL,
    legend = FALSE
) {
    assert_is_matrix(raw)
    assert_is_matrix(normalized)
    assert_are_identical(dimnames(normalized), dimnames(raw))
    assert_is_any_of(vst, c("matrix", "NULL"))
    if (is.matrix(vst)) {
        assert_are_identical(dimnames(vst), dimnames(raw))
    }
    assert_is_any_of(rlog, c("matrix", "NULL"))
    if (is.matrix(rlog)) {
        assert_are_identical(dimnames(rlog), dimnames(raw))
    }
    assert_is_a_bool(legend)

    xlab <- "rank (mean)"
    nonzero <- rowSums(raw) > 0L

    # DESeq2 log2 normalized.
    gglog2 <- normalized %>%
        .[nonzero, , drop = FALSE] %>%
        `+`(1L) %>%
        log2() %>%
        meanSdPlot(plot = FALSE) %>%
        .[["gg"]] +
        ggtitle("DESeq2 log2") +
        xlab(xlab)

    # DESeq2 regularized log.
    if (is.matrix(rlog)) {
        ggrlog <- rlog %>%
            .[nonzero, , drop = FALSE] %>%
            meanSdPlot(plot = FALSE) %>%
            .[["gg"]] +
            ggtitle("DESeq2 rlog") +
            xlab(xlab)
    } else {
        message("Skipping regularized log.")
        ggrlog <- NULL
    }

    # DESeq2 variance stabilizing transformation.
    if (is.matrix(vst)) {
        ggvst <- vst %>%
            .[nonzero, , drop = FALSE] %>%
            meanSdPlot(plot = FALSE) %>%
            .[["gg"]] +
            ggtitle("DESeq2 vst") +
            xlab(xlab)
    } else {
        message("Skipping variance stabilizing transformation.")
        ggvst <- NULL
    }

    # edgeR log2 tmm.
    ggtmm <- raw %>%
        tmm() %>%
        .[nonzero, , drop = FALSE] %>%
        `+`(1L) %>%
        log2() %>%
        meanSdPlot(plot = FALSE) %>%
        .[["gg"]] +
        ggtitle("edgeR log2 tmm") +
        xlab(xlab)

    plotlist <- list(
        log2 = gglog2,
        rlog = ggrlog,
        vst = ggvst,
        tmm = ggtmm
    )
    # Remove NULL assays if present (e.g. rlog, vst).
    plotlist <- Filter(Negate(is.null), plotlist)

    # Remove the plot (color) legend, if desired.
    if (!isTRUE(legend)) {
        plotlist <- lapply(plotlist, function(p) {
            p <- p + theme(legend.position = "none")
        })
    }

    plot_grid(plotlist = plotlist)
}



# bcbioRNASeq ==================================================================
# Require that the DESeq2 transformations are slotted.
# If `transformationLimit` was applied, this function will error.
plotMeanSD.bcbioRNASeq <-  # nolint
    function(
        object,
        legend = getOption("basejump.legend", FALSE)
    ) {
        .plotMeanSD(
            raw = counts(object, normalized = FALSE),
            normalized = counts(object, normalized = TRUE),
            rlog = assays(object)[["rlog"]],
            vst = assays(object)[["vst"]],
            legend = legend
        )
    }



#' @rdname plotMeanSD
#' @export
setMethod(
    f = "plotMeanSD",
    signature = signature("bcbioRNASeq"),
    definition = plotMeanSD.bcbioRNASeq
)



# DESeqDataSet =================================================================
plotMeanSD.DESeqDataSet <-  # nolint
    function(
        object,
        vst = TRUE,
        rlog = FALSE,
        legend = getOption("basejump.legend", FALSE)
    ) {
        validObject(object)
        assert_is_a_bool(vst)
        if (isTRUE(vst)) {
            vst <- assay(varianceStabilizingTransformation(object))
        } else {
            vst <- NULL
        }
        assert_is_a_bool(rlog)
        if (isTRUE(rlog)) {
            rlog <- assay(rlog(object))
        } else {
            rlog <- NULL
        }
        .plotMeanSD(
            raw = counts(object, normalized = FALSE),
            normalized = counts(object, normalized = TRUE),
            vst = vst,
            rlog = rlog,
            legend = legend
        )
    }



#' @rdname plotMeanSD
#' @export
setMethod(
    f = "plotMeanSD",
    signature = signature("DESeqDataSet"),
    definition = plotMeanSD.DESeqDataSet
)



# DESeqAnalysis ================================================================
#' @rdname plotMeanSD
#' @export
setMethod(
    f = "plotMeanSD",
    signature = signature("DESeqAnalysis"),
    definition = function(object) {
        plotMeanSD(as(object, "DESeqDataSet"))
    }
)
